package cidc.pqr.ws_Bizagi_obj;


public class HistoricoDatos {
	

	private String idHistorico;
	private String accionHistorico;
	private String fechaHistorico;
	private String usuario;
	private String idSolicitud;
	private String entrada;
	
	
	public String getFechaHistorico() {
		return fechaHistorico;
	}
	public void setFechaHistorico(String fechaHistorico) {
		this.fechaHistorico = fechaHistorico;
	}
	public String getIdHistorico() {
		return idHistorico;
	}
	public void setIdHistorico(String idHistorico) {
		this.idHistorico = idHistorico;
	}
	public String getAccionHistorico() {
		return accionHistorico;
	}
	public void setAccionHistorico(String accionHistorico) {
		this.accionHistorico = accionHistorico;
	}
	public String getUsuario() {
		return usuario;
	}
	public void setUsuario(String usuario) {
		this.usuario = usuario;
	}
	public String getIdSolicitud() {
		return idSolicitud;
	}
	public void setIdSolicitud(String idSolicitud) {
		this.idSolicitud = idSolicitud;
	}
	public String getEntrada() {
		return entrada;
	}
	public void setEntrada(String entrada) {
		this.entrada = entrada;
	}

	
}
